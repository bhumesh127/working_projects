# What is Imbalance Ratio?

# Imbalance Ratio (IR) = The ratio between the majority class and minority class in your dataset.

# Simple Formula
# Imbalance Ratio = Majority Class Count / Minority Class Count

# Real-World Analogy

# Imagine a hospital has 1000 patients:

# 970 healthy patients
# 30 diabetic patients

# Imbalance Ratio = 970 / 30 = 32:1
# → For every 1 diabetic patient, there are 32 healthy ones!